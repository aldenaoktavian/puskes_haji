   <h3>Pro Rakyat</h3>
  </div>
  <div class="colelem" id="u7641"><!-- simple frame --></div>
  <div class="Paragraph--16px clearfix colelem" id="u7618-4"><!-- content -->
   <p>Dalam penyelenggaraan pembangunan kesehatan, Kementerian Kesehatan selalu mendahulukan kepentingan rakyat dan harus menghasilkan yang terbaik untuk rakyat. Diperolehnya derajat kesehatan yang setinggi-tingginya bagi setiap orang adalah salah satu hak asasi manusia tanpa membedakan suku, golongan, agama dan status sosial ekonomi.</p>
  </div>
  <div class="Heading-3--28px clearfix colelem" id="u7656-4"><!-- content -->
   <h3>Inklusif</h3>
  </div>
  <div class="colelem" id="u7653"><!-- simple frame --></div>
  <div class="Paragraph--16px clearfix colelem" id="u7659-4"><!-- content -->
   <p>Semua program pembangunan kesehatan harus melibatkan semua pihak, karena pembangunan kesehatan tidak mungkin hanya dilaksanakan oleh Kementerian Kesehatan saja. Dengan demikian, seluruh komponen masyarakat harus berpartisipasi aktif, yang meliputi lintas sektor, organisasi profesi, organisasi masyarakat pengusaha, masyarakat madani dan masyarakat akar rumput.</p>
  </div>
  <div class="Heading-3--28px clearfix colelem" id="u7668-4"><!-- content -->
